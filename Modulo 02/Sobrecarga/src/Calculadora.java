public class Calculadora {
    //Metodo para somar dois numeros inteiros
    public int somar(int a, int b){
        return a + b;
    }
    //Metodo para somar 3 numeros inteiros
    public int somar(int a, int b, int c){
        return a + b + c;
    }

    //Metodo para somar 2 numeros double
    public double somar(double a, double b){
        return a + b;
    }
}
